// Definindo uma variável 'vel' e atribuindo o valor 78 a ela, representando a velocidade do carro em Km/h.
var vel = 78

// Exibindo a mensagem que inclui a velocidade do carro no console.
console.log(`A velocidade do seu carro é ${vel}Km/h`)

// Iniciando uma estrutura condicional 'if' para verificar se a velocidade é maior que 60 Km/h.
if (vel > 60) {
    // Se a condição for verdadeira, exibe uma mensagem indicando que o motorista ultrapassou a velocidade permitida e está sendo multado.
    console.log(`Você ultrapassou a velocidade permitida. Multado!`)
}

// Exibindo uma mensagem no console que lembra o motorista de usar o cinto de segurança, independentemente da velocidade.
console.log(`Dirija sempre usando cinto de segurança!`)

/* Neste código, a velocidade do carro é armazenada na variável 'vel', e uma mensagem é exibida no console indicando a velocidade do carro. Em seguida, uma estrutura condicional 'if' verifica se a velocidade é maior que 60 Km/h e, se for verdadeira, exibe uma mensagem informando que o motorista está sendo multado. Após a estrutura condicional, uma mensagem final lembra o motorista de usar sempre o cinto de segurança. */