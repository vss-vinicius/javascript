/* 
Este é um loop "for" que inicializa a variável 'c' com o valor 1. 
O loop continuará enquanto 'c' for menor ou igual a 5. 
A cada iteração, 'c' é incrementado em 1.
*/

for (c = 1; c <= 5; c++) {
    // Esta linha imprime no console uma mensagem que inclui o valor atual de 'c'.
    console.log(`Número ${c}`);
}